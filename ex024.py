n1 = float(input('Digite um número: '))
n2 = float(input('Digite outro número: '))

print('A divisão de {} e {} é {}.'.format(n1 / n2), end='>>>')

print('A multiplicação de {} e {} é {}.'.format(n1 * n2), end='>>>')

print('A divisão inteira de {} e {} é {}.'.format(n1 // n2), end='>>>')

print('O resto da divisão de {} e {} é {}.'.format(n1 % n2), end='>>>')
