#Vai me mostrar qual é o tipo: (str, int, bool, float):

n = str(input('Digite um valor: '))
print(type(n))

#Vai me mostrar que é um int:

n = int(input('Digite um valor: '))
print(type(n))

#Vai dizer se é verdadeiro (true) ou falso (false):

n = bool(input('Digite um valor: '))
print(n)

#Vai converter qualquer número para número fracionado:

n = float(input('Digite um valor: '))
print(n)
